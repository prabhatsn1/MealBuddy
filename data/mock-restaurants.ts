/**
 * Returns all mock results for a given food query.
 * Matches by cuisine keyword (case-insensitive).
 */
export function getMockResults(query: string): RestaurantResult[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  const keywordMap: Record<string, string> = {
    biryani: 'biryani',
    pizza: 'pizza',
    burger: 'burger',
    dosa: 'dosa',
    roll: 'rolls',
    rolls: 'rolls',
    kathi: 'rolls',
    frankie: 'rolls',
    wrap: 'rolls',
  };

  // Find the cuisine from the keyword map first (exact match), then fallback to partial
  let cuisine: string | undefined;
  for (const [keyword, mapped] of Object.entries(keywordMap)) {
    if (q.includes(keyword)) {
      cuisine = mapped;
      break;
    }
  }

  // Fallback: match cuisine name directly
  if (!cuisine) {
    const cuisines = [...new Set(ALL_RESTAURANTS.map((r) => r.cuisine))];
    cuisine = cuisines.find((c) => c.includes(q) || q.includes(c));
  }

  if (!cuisine) return [];
  return ALL_RESTAURANTS.filter((r) => r.cuisine === cuisine);
}
